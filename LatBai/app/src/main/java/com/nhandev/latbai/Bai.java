package com.nhandev.latbai;

public class Bai {
    private int id;
    private int point;

    public Bai(int id, int point) {
        this.id = id;
        this.point = point;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public int getPoint() {
        return point;
    }

    public void setPoint(int point) {
        this.point = point;
    }
}
